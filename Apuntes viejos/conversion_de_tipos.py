tupla = (1, 2, 3, 4, 5)
print (tupla)

print (list(tupla)) # Convierte la Tupla en una Lista

print("----")

lista = [1, 2, 3, 4, 5]
print (lista)

print (tuple(lista)) # Convierte la Lista en una Tupla

print("----")

# Concadenación simple de colecciones


tupla2 = (6, 7, 8, 9, 10)
print (tupla + tupla2) # Une varias colecciones de un mismo tipo

print("----")

lista2 = [6, 7, 8, 9, 10]
print (lista + lista2)

print("----")

# Valor máximo y mínimo
print (max(tupla)) # El valor más alto en esta tupla es 5
print (min(lista2)) # El valor de menor valor es 6

print("----")


